def normalize_language(language: str | None) -> str:
    if not language:
        return DEFAULT_LANGUAGE
    normalized = language.replace("-", "_").split("_", 1)[0].lower()
    return normalized if normalized in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def language_from_locale(locale_name: str | None) -> str:
    return normalize_language(locale_name)


@dataclass(frozen=True, slots=True)
class Translator:
    language: str = DEFAULT_LANGUAGE

    def __post_init__(self) -> None:
        object.__setattr__(self, "language", normalize_language(self.language))

    def text(self, key: str) -> str:
        return TRANSLATIONS.get(self.language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(
            key,
            TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key),
        )
